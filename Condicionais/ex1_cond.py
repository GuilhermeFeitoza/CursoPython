numero1 = int(input("Digite o primeiro numero"))
numero2 = int(input("Digite o segundo numero"))

if numero1 > numero2:
    print(f'{numero1} é maior que{numero2}')

if numero2 > numero1:
    print(f'{numero2} é maior que {numero1}')

if numero1 == numero2:
    print(f'{numero1} é igual a {numero2}')
